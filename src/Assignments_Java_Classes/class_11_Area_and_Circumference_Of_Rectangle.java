package Assignments_Java_Classes;

public class class_11_Area_and_Circumference_Of_Rectangle 
{
	static double l = 34.34;
	static double b = 16.66;
	public static void main(String[] args) 
	{
		double area_of_rectangle = l*b;
		System.out.println("Area Of Rectangle= "+area_of_rectangle);
		double perimeter_of_rectangle = 2*(l+b);
		System.out.println("Perimeter Of Rectangle= "+perimeter_of_rectangle);
	}
}
