package Assignments_Java_Classes;

public class class_11_Area_and_Circumference_Of_Trapezium 
{
	static double half = 0.5;
	static double a = 843;
	static double b = 676;
	static double c = 343;
	static double d = 234;
	static double h = 686;
	public static void main(String[] args) 
	{
		double AreaofTrapezium = half*(a+b)*h;
		System.out.println("Area_of_Trapezium= "+AreaofTrapezium);
		
		double perimeterofTrapezium = a+b+c+d;
		System.out.println("Area_of_Trapezium= "+perimeterofTrapezium);
	}
}
