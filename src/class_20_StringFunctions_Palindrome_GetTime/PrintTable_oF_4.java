package class_20_StringFunctions_Palindrome_GetTime;

import java.util.Scanner;

public class PrintTable_oF_4 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int a = 4;
		for (int i = 15; i <= n; i++) {
			System.out.println(a);
			a = a + 4;
		}
	}
}
