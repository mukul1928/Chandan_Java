package class_06_DeclareAndInitializeEachDataTypes_Operators_ConditionalStatements;

public class DeclareAndInitialize_EachDataTypes {
	public static void main(String[] args) {
		byte a = 10;// declaration and initialization of a variable of 'a'
		System.out.println(a);
		short b = 32123;
		System.out.println(b);
		int c = 98765;
		System.out.println(c);
		long d = 908765;
		System.out.println(d);
		float e = 5.43f;
		System.out.println(e);
		double a1 = 876.432;
		System.out.println(a1);
		char b1 = 'J';
		System.out.println(b1);
		boolean c1 = true;
		System.out.println(c1);
		boolean c2 = false;
		System.out.println(c2);
		String name = "Chandan Pandey";
		System.out.println(name);

	}

}
